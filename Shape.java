// Parent class
class Shape {
    //Fields
    private double x;
    private double y;

    // Constructor
    public Shape(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // Getters for x and y
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
